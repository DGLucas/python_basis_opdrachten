# Eerste set waarden
c1 = -12
f1 = 144

# Conversie van Celsius naar Fahrenheit
fahrenheit1 = (c1 * 9/5) + 32

# Conversie van Fahrenheit naar Celsius
celsius1 = (f1 - 32) * 5/9

# Uitvoer voor de eerste set waarden
print(f"{c1} graden Celsius is gelijk aan {fahrenheit1:.1f} graden Fahrenheit") #De 1.f staat voor 1 getal na de ,
print(f"{f1} graden Fahrenheit is gelijk aan {celsius1:.1f} graden Celsius") #De 1.f staat voor 1 getal na de ,

# Tweede set waarden
c2 = 62.2
f2 = 96

# Conversie van Celsius naar Fahrenheit
fahrenheit2 = (c2 * 9/5) + 32

# Conversie van Fahrenheit naar Celsius
celsius2 = (f2 - 32) * 5/9

# Uitvoer voor de tweede set waarden
print(f"{c2:.1f} graden Celsius is gelijk aan {fahrenheit2:.1f} graden Fahrenheit") #De 1.f staat voor 1 getal na de ,
print(f"{f2} graden Fahrenheit is gelijk aan {celsius2:.1f} graden Celsius") #De 1.f staat voor 1 getal na de ,

#De 'f' aan het begin van de string geeft aan dat dit een geformatteerde string is. De waarden van de variabelen c1 en fahrenheit1 worden tussen de accolades {} ingevoegd
